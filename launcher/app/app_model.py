from distutils.version import LooseVersion
from launcher.tools.version import Version


def is_up_to_date(current_version, server_version):
    if current_version == Version.NOT_INSTALLED_VERSION:
        return False
    return LooseVersion(current_version) >= LooseVersion(server_version)
